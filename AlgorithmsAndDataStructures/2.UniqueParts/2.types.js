// PRIMITIVE DATA TYPES
// In JS there is 7 types of primitive DATA they are:
// boolean, number, string, undefined,
// object, function, and symbol

var is2020 = false;
typeof is2020; // boolean

var age = 20;
typeof age; // number

var lastname = "Charris";
typeof lastname; // string

var fruits = ["Apple", "Bannana", "Orange"];
typeof fruits; // object

var nullVar = null;
typeof nullVar; // object

var someFunctions = () => {};
typeof someFunctions; // function

var blank;
typeof blank; // undefined

